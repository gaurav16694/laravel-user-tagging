     */
    public function update(Request $request, Comment $comment)
    {
        $attributes = $request->validate([
            'text' => 'required'
        ]);

        tap($comment)
            ->update($attributes)
            ->clearMentions()
            ->mention($request->mentions);

        return back();
    }

